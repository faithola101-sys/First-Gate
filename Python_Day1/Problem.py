def add(a, b):
	return a + b

def sub(a, b):
	return a - b

def mult(a, b):
	return a * b

def divide(a, b):
	return a / b

def modul(a, b):
	return a % b

def even(a):
	return a % 2 == 0

def odd(b):
	return b % 2 != 0
print(add(3,2))
print(odd(3,2))
print(even(3))
print(modul(3,2))
print(divide(3,2))
print(sub(3,2))
